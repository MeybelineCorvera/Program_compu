let numday = 1;
let Day;

switch(numday){
    case 1:
        Day = "Lunes";
        break;
    case 2:
        Day = "Martes";
        break;
    case 3:
        Day = "Miercoles";
        break;
    case 4:
        Day = "Jueves";
        break;
    case 5:
        Day = "Virnes";
        break;
    case 6:
        Day = "Sabado";
        break;
    case 7:
        Day = "Domingo";
        break;
    default:
        Day = "Dia invalido";
}

console.log(Day);